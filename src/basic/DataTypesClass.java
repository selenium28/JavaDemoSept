package basic;

public class DataTypesClass {

	// Starting point - main method().
	public static void main(String[] args) {
		// DataTypes
		// Primitive Data types and non-Primitive Data types
		
		// int - int type of data, i- is a variable, 10- is a value.
		// Duplicate variable is not allowed.
		// 1. DataTypes - int
		int k; // Initiation
		int i = 10; // declaration 
		int a = 100;
		int b = -12;
		System.out.println(a);
		
		// 2. DataTypes - double
		
		double d = 12.50;
		double d1 = 33.33;
		double d2 = 100;
		System.out.println("d2: "+d2);
		
		// 3. DataTypes - char
		char c = 'd';
		char c1 = 'e';
		char c2 = 'v';
		System.out.println(c);
		
		// Boolean is a true or false. are the keywords or values?
		// Yes they are keywords in java but we can use as values.
		// 4. DataTypes - boolean
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b2);
		
		// String is class but we can use as a datatype
		String str = "dev";
		String str2 = "java";
		String str3 ="35547gj878756734##$$%^&";
		System.out.println(str3);
	
	}

}
