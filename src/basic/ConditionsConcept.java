package basic;

public class ConditionsConcept {

	public static void main(String[] args) {
		
		int a = 200;
		int b = 100;
		
		if (a>b) {
			System.out.println("Test Passed");
		} else {
			System.out.println("Test Failed");
		}
		
		// = -
		// == - 
		
		int m = 50;
		int n = 100;
		
		if (m==n) {
			System.out.println("m&n matched");
		} else {
			System.out.println("m&n not matched");
		}

	}

	
	
	
}
